package com.ntels.ccbs.batch.iv.common.entity;

import java.sql.Timestamp;

public class OverpayBillAply {

	/**
	 * 해당 청구월, 청구주기에 청구대상이 되는 납부계정 정보에 대한 청구서 번호(20자리) 청구년월(4) + 주기(2) +
	 * 청구일자DD(2) + 납부계정ID(10) + 일련번호(2)
	 */
	private String billSeqNo;

	/**
	 * SO_ID or RO_ID
	 */
	private String soId;

	/**
	 * 0: 당월 1: 전월 2: 전전월
	 */
	private String billMmTp;

	/**
	 * Table Sequence 번호
	 */
	private String prepayOccSeqNo;

	/**
	 * 청구년월(YYYYMM)
	 */
	private String billYymm;

	/**
	 * 청구주기
	 */
	private String billCycl;

	/**
	 * 청구일자(YYYYMMDD)
	 */
	private String billDt;

	/**
	 * 납부계정ID
	 */
	private String pymAcntId;

	/**
	 * 선수금잔액
	 */
	private Double prepayBal;

	/**
	 * 청구적용금액
	 */
	private Double billAplyAmt;

	/**
	 * 청구생성일자(YYYYMMDD)
	 */
	private String billCrtDt;

	/**
	 * 수납처리여부 N: 수납미처리 Y: 수납처리완료
	 */
	private String payProcYn;

	/**
	 * 수납처리일자(YYYYMMDD)
	 */
	private String payProcDt;

	/**
	 * 등록일
	 */
	private Timestamp regDate;

	/**
	 * 이전지점ID
	 */
	private String preSoId;

	/**
	 * 단체ID
	 */
	private String grpId;

	/**
	 * 고객ID
	 */
	private String custId;

	/**
	 * 계약ID
	 */
	private String ctrtId;
	
	private String transDt;
	private String dpstDt;

	public String getBillSeqNo() {
		return billSeqNo;
	}

	public void setBillSeqNo(String billSeqNo) {
		this.billSeqNo = billSeqNo;
	}

	public String getSoId() {
		return soId;
	}

	public void setSoId(String soId) {
		this.soId = soId;
	}

	public String getBillMmTp() {
		return billMmTp;
	}

	public void setBillMmTp(String billMmTp) {
		this.billMmTp = billMmTp;
	}

	public String getPrepayOccSeqNo() {
		return prepayOccSeqNo;
	}

	public void setPrepayOccSeqNo(String prepayOccSeqNo) {
		this.prepayOccSeqNo = prepayOccSeqNo;
	}

	public String getBillYymm() {
		return billYymm;
	}

	public void setBillYymm(String billYymm) {
		this.billYymm = billYymm;
	}

	public String getBillCycl() {
		return billCycl;
	}

	public void setBillCycl(String billCycl) {
		this.billCycl = billCycl;
	}

	public String getBillDt() {
		return billDt;
	}

	public void setBillDt(String billDt) {
		this.billDt = billDt;
	}

	public String getPymAcntId() {
		return pymAcntId;
	}

	public void setPymAcntId(String pymAcntId) {
		this.pymAcntId = pymAcntId;
	}

	public Double getPrepayBal() {
		return prepayBal;
	}

	public void setPrepayBal(Double prepayBal) {
		this.prepayBal = prepayBal;
	}

	public Double getBillAplyAmt() {
		return billAplyAmt;
	}

	public void setBillAplyAmt(Double billAplyAmt) {
		this.billAplyAmt = billAplyAmt;
	}

	public String getBillCrtDt() {
		return billCrtDt;
	}

	public void setBillCrtDt(String billCrtDt) {
		this.billCrtDt = billCrtDt;
	}

	public String getPayProcYn() {
		return payProcYn;
	}

	public void setPayProcYn(String payProcYn) {
		this.payProcYn = payProcYn;
	}

	public String getPayProcDt() {
		return payProcDt;
	}

	public void setPayProcDt(String payProcDt) {
		this.payProcDt = payProcDt;
	}

	public Timestamp getRegDate() {
		return regDate;
	}

	public void setRegDate(Timestamp regDate) {
		this.regDate = regDate;
	}

	public String getPreSoId() {
		return preSoId;
	}

	public void setPreSoId(String preSoId) {
		this.preSoId = preSoId;
	}

	public String getGrpId() {
		return grpId;
	}

	public void setGrpId(String grpId) {
		this.grpId = grpId;
	}

	public String getCustId() {
		return custId;
	}

	public void setCustId(String custId) {
		this.custId = custId;
	}

	public String getCtrtId() {
		return ctrtId;
	}

	public void setCtrtId(String ctrtId) {
		this.ctrtId = ctrtId;
	}

	public String getTransDt() {
		return transDt;
	}

	public void setTransDt(String transDt) {
		this.transDt = transDt;
	}

	public String getDpstDt() {
		return dpstDt;
	}

	public void setDpstDt(String dpstDt) {
		this.dpstDt = dpstDt;
	}

}
